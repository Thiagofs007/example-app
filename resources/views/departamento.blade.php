                        <h2 class="text-center"> Atibuições da Direção Escolar</h2><br><br>

                        <p class="text-start"><strong>De acordo com o artigo 20 do Regimento Comum das Etecs, o Diretor
                                de Etec, sem prejuízo de outras constantes em documento próprio do CEETEPS e da
                                legislação, terá as seguintes atribuições:<br><br>

                                1 – garantir as condições para o desenvolvimento da gestão democrática do ensino, na
                                forma prevista pela legislação e neste Regimento;<br>
                                2 – promover a elaboração do projeto político-pedagógico da escola;<br>
                                3 – coordenar a elaboração, o acompanhamento, a avaliação e o controle da execução do
                                Plano Plurianual de Gestão;<br>
                                4 – gerenciar os recursos físicos, materiais, humanos e financeiros para atender às
                                necessidades da escola a curto, médio e longo prazos;<br>
                                5 – coordenar o planejamento, execução, controle e avaliação das atividades da escola
                                com o objetivo de garantir a melhoria dos processos;<br>
                                6 – garantir o cumprimento dos conteúdos curriculares, das cargas horárias e dos dias
                                letivos previstos na legislação vigente;<br>
                                7 – garantir os meios para a recuperação de alunos de menor rendimento e em progressão
                                parcial, seja ela na forma presencial ou virtual.<br>
                                8 – assegurar o cumprimento da legislação, bem como dos regulamentos, diretrizes e
                                normas emanadas da administração superior e da Administração Central do CEETEPS;<br>
                                9 – expedir diplomas, certificados e outros documentos escolares, responsabilizando-se
                                por sua autenticidade e exatidão;<br>
                                10 – promover estudos fundamentados por indicadores institucionais e gerenciar ações
                                visando à oferta de cursos e seu contínuo aprimoramento, assim como de programas, dos
                                recursos físicos, materiais e humanos que compõem a escola;<br>
                                11 – administrar e responsabilizar-se pelo patrimônio do CEETEPS, observadas as normas e
                                diretrizes legais e infralegais aplicáveis, fomentando estratégias para controle,
                                manutenção, preservação e desfazimento dos bens inservíveis;<br>
                                12 – promover ações para a integração escola-família e comunidade-empresa, apoiado em
                                indicadores de fontes reconhecidas;<br>
                                13 – coordenar a elaboração de projetos, submetendo-os à aprovação dos órgãos
                                competentes, acompanhar seu desenvolvimento e avaliar seus resultados;<br>
                                14 – criar condições e estimular experiências inovadoras para o aprimoramento do
                                processo educacional;<br>
                                15 – integrar as ações dos serviços prestados pela escola;<br>
                                16 – criar procedimentos que favoreçam a comunicação e o fluxo de informações junto à
                                Comunidade Escolar;<br>
                                17 – convocar para as reuniões ordinárias e extraordinárias, de forma presencial ou
                                virtual, os representantes dos órgãos colegiados discentes, docentes, servidores e
                                membros dos demais segmentos da comunidade escolar;<br>
                                18 – desempenhar outras atividades correlatas e afins.<br><br></strong></p>
