                                <h2 class="text-center">Contabilidade</h2><br><br>

                                <p class="text-center"><strong>O curso de contabilidade demanda profundos conhecimentos
                                        em matemática e língua portuguesa, constituindo-se como uma base sólida para
                                        compreender e dominar os princípios fundamentais dessa ciência. Por meio da
                                        matemática, os estudantes adquirem as ferramentas essenciais para calcular
                                        lucros, prejuízos e realizar operações financeiras cruciais. Já a língua
                                        portuguesa desempenha um papel central na interpretação precisa das normas
                                        contábeis e na redação de relatórios e documentos contábeis.
                                        Além desses aspectos, a compreensão da contabilidade transcende o domínio
                                        técnico, abrangendo também o estudo das leis e regulamentos que regem as
                                        práticas contábeis, bem como as obrigações fiscais e tributárias impostas às
                                        empresas. Assim, os futuros profissionais não apenas aprendem a aplicar as
                                        normas contábeis, mas também compreendem as implicações legais e éticas de suas
                                        atividades.
                                        Na área da informática, os estudantes são capacitados no uso de softwares
                                        especializados em contabilidade, tornando-os aptos a analisar dados financeiros
                                        e gerar relatórios precisos em um contexto empresarial cada vez mais
                                        digitalizado.
                                        Logo, o curso de contabilidade oferece uma formação completa e abrangente, que
                                        combina conhecimentos técnicos, habilidades tecnológicas e uma compreensão
                                        profunda das leis e regulamentos contábeis, preparando os alunos para desafios
                                        exigentes e para contribuir efetivamente para o sucesso das organizações em que
                                        atuam. <br><br></strong></p>
